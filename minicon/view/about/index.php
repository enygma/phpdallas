<h2>About the Conference</h2>

<p>
The DallasPHP MiniCon conference has been created to bring some of the 
best PHP and web-related content to the Dallas/Ft. Worth area in a single
day event packed with content.
</p>
<p>
The conference is currently in the planning stages, but rest assured we're 
working hard to get the best speakers and best location we can. If you have any
questions or would like to contact the conference staff, you can email
<a href="mailto:info@phpdallas.org">info@phpdallas.org</a>.
</p>
